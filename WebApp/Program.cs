using Blazorise;
using Blazorise.Icons.FontAwesome;
using Blazorise.Tailwind;
using Microsoft.Extensions.FileProviders;
using System.Reflection;
using WebApp.Components;
using WebApp.Services;

var builder = WebApplication.CreateBuilder(args);

// Add services to the container.
builder.Services.AddRazorComponents()
    .AddInteractiveServerComponents();

// Add Blazorise
builder.Services
    .AddBlazorise(options =>
    {
        options.Debounce = true;
        options.DebounceInterval = 300;
    })
    .AddTailwindProviders()
    .AddFontAwesomeIcons();

builder.Services
    .AddControllers();

builder.Services
    .AddSwaggerGen(c =>
    {
        //Locate the XML file being generated by ASP.NET...
        var xmlFile = $"{Assembly.GetExecutingAssembly().GetName().Name}.XML";
        var xmlPath = Path.Combine(AppContext.BaseDirectory, xmlFile);
        
        //... and tell Swagger to use those XML comments.
        c.IncludeXmlComments(xmlPath, includeControllerXmlComments: true);
    });

builder.Services.AddScoped<CookieService>();
builder.Services.AddScoped<CurrentSession>();
builder.Services.AddScoped<ChatService>();

// Add custom SiteSetting service
builder.Configuration.AddSiteBinding();

// Configure the HTTP request pipeline.
var app = builder.Build();
if (!app.Environment.IsDevelopment())
{
    app.UseExceptionHandler("/Error", createScopeForErrors: true);
    // The default HSTS value is 30 days. You may want to change this for production scenarios, see https://aka.ms/aspnetcore-hsts.
    app.UseHsts();
}

app.UseHttpsRedirection();

app.UseStaticFiles();
app.UseAntiforgery();

app.MapRazorComponents<App>()
    .AddInteractiveServerRenderMode();


app.UseStaticFiles(new StaticFileOptions()
{
    ServeUnknownFileTypes = true,
    FileProvider = new PhysicalFileProvider(RezApi.Settings.StoragePath),
    RequestPath = new PathString("/files")
});

app.MapControllers();
app.UseSwagger();
app.UseSwaggerUI(options =>
{
    options.SwaggerEndpoint("/swagger/v1/swagger.json", "v1");
});
app.Run();
